 //example on arthimetic operations
 #include <stdio.h>
 void main()
 {  
   int a,b,res;
   a=10;
   b=5;
   res=a+b;
   printf("the sum is %d\n",res);
   res=a-b;
   printf("the difference is %d\n",res);
   res=a*b;
   printf("the product is %d\n",res);
   res=a/b;
   printf("the division is %d\n",res);
}
